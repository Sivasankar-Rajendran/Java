package JavaWithMOsh;

public class M01_Variables {

    public static void main(String[] args) {
        int age = 25;
        age = 40;

        // int = variable type
        // age = variable name or identifier
        // 25 = variable value
        // variable name should be camelCase

        int myAge = age;
        System.out.println(age);
        System.out.println(myAge);
    }

}

package W3Schools.JavaTutorial;

public class Java08DataTypes {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int c = a + b;

        int aa = 250;
        int bb = 450;
        int cc = aa + bb;

        int d = b - a;

        System.out.println(c);
        System.out.println(d);
        System.out.println(cc);

    }

}
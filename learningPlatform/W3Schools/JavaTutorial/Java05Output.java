package W3Schools.JavaTutorial;

public class Java05Output {

    public static void main(String[] args) {

        // The below details will print in the same line
        System.out.print("Hello World..!");
        System.out.print("This is next line");

        // The below details will print in separate lines
        System.out.println("Hello New World..!");
        System.out.println("This is for Java Learing..!");

        // Text must be wrapped inside double quotations marks ""
        // we don't put numbers inside double quotes
        System.out.println(3 + 3);
        System.out.println(54 * 4);
    }

}

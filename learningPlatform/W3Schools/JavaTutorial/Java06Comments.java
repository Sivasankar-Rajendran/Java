package W3Schools.JavaTutorial;

public class Java06Comments {

    public static void main(String[] args) {

        // Single-line Comments (ShortCut: Ctrl + /)
        // Single-line comments start with two forward slashes (//).
        // Any text between // and the end of the line is ignored by Java (will not be
        // executed).

        System.out.println("Hello Comments");
        // System.out.println("I dont want print in the output");

        // Java Multi-line Comments (ShortCut: Shift + Alt + A)
        // Multi-line comments start with /* and ends with */.
        // Any text between /* and */ will be ignored by Java.

        /*
         * System.out.println("Print Multiline 1");
         * System.out.println("Print Multiline 2");
         * System.out.println("Print Multiline 3");
         * System.out.println("Print Multiline 4");
         */
        System.out.println("Print Multiline 5");

    }

}
